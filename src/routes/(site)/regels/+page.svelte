	<section>
		<h1>Puntentelling</h1>
		<ul>
			<li>
				De disputen proberen tijdens de week zoveel mogelijk bierdopjes te behalen. Het dispuut met
				na afloop het meest aantal bierdopjes wint de DCW. Het winnende dispuut ontvangt de
				wisselbokaal en een fust bier. De gelukkige winnaar wordt op vrijdag 21 april bekend gemaakt
				op SSR.
			</li>
			<li>
				Een dispuut verdient bierdopjes door een goed resultaat neer te zetten bij de activiteiten
				en opdrachten. Aanwezigheid bij een activiteit is op zichzelf al een klein aantal bierdopjes
				waard, maar het overgrote merendeel van de bierdopjes is afhankelijk van hoe een dispuut
				presteert bij die specifieke activiteit.
			</li>
			<li>
				Bij de eerste activiteit op de maandag knutselt ieder dispuut een item. Een dispuut kan elke
				dag extra bierdopjes verdienen door met hun item een foto te maken bij een bepaalde plek
				ergens in Leiden. Deze plek zal dagelijks op de website en op de insta bekend gemaakt
				worden, gelijktijdig met de dagopdrachten om 10u00.
			</li>
			<li>
				De behaalde bierdopjes per dispuut zijn na de opdrachten te zien op de website. De
				tussentijdse score wordt de hele week door de DCW-redactie op de DCW-website geüpdatet.
			</li>
			<li>
				Het staat de DCW-redactie altijd vrij om aanpassingen te maken in het bierdopjesaantal van
				een dispuut, bijvoorbeeld bij onsportief gedrag. Deze aanpassingen zullen op een
				transparante wijze plaatsvinden.
			</li>
			<li>Vragen over de puntentelling gaan via de mail: dcw@ssr-leiden.nl.</li>
		</ul>
	</section>
	<section>
		<h1>Jokers</h1>
		<ul>
			<li>
				Positieve en negatieve jokers zijn te verdienen door jokerdag- en jokerweekopdrachten uit te
				voeren.
			</li>
			<li>Het is niet mogelijk om op vrijdag een positieve of negatieve joker in te zetten.</li>
			<li>
				Op maandag, dinsdag en woensdag verschijnen om 10u00 de vijf dagopdrachten op de DCW-site.
				Vier van de dagopdrachten zijn bierdopjes waard, één van de dagopdrachten is een
				jokerdagopdracht. Op donderdag is er geen jokerdagopdracht, enkel vier normale
				dagopdrachten.
			</li>
			<li>
				De jokerdagopdracht is geen bierdopjes waard, maar kan bij goede uitvoering tot de winst van
				een positieve of negatieve joker leiden. De top drie best uitgevoerde jokeropdrachten
				verdienen een joker.
			</li>
			<li>
				De DCW-redactie bepaalt welke disputen de dag- en weekopdrachten het best hebben uitgevoerd.
				Het loont dus om inzet te tonen!
			</li>
			<li>
				Wanneer een dispuut een joker verdient, wordt deze de volgende dag direct ingezet. Het
				dispuut mag kiezen op welke van de twee activiteiten de joker wordt ingezet.
			</li>
			<li>
				Er zijn vijf weekopdrachten, waarvan één de jokerweekopdracht is. De weekopdrachten kunnen
				op maandag, dinsdag, woensdag en donderdag worden uitgevoerd en moeten worden gestuurd naar
				de DCW-mail.
			</li>
			<li>
				Het goed uitvoeren van de jokerweekopdracht leidt automatisch tot het verdienen van een
				positieve joker.
			</li>
			<li>
				De DCW-redactie bepaalt welke disputen de dag- en weekjokeropdrachten het beste hebben
				uitgevoerd en dus een joker verdienen.
			</li>
			<li>De eisen betreffende de dag- en weekopdrachten worden op de DCW-site bekend gemaakt.</li>
			<li>
				Persoonlijke opdrachten over de DCW-redactieleden dienen naar de DCW-mail gestuurd te
				worden.
			</li>
			<li>Vragen over het jokersysteem gaan via de mail: dcw@ssr-leiden.nl.</li>
		</ul>
